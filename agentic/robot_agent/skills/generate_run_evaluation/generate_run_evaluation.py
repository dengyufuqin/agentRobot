#!/usr/bin/env python3
"""Generate a run_evaluation.sh that spins up the policy server + runs the
benchmark client in a single script.

Inputs describe both sides:
  POLICY side:
    --policy-repo         : repo path
    --policy-entry        : entry script (policy_server.py)
    --checkpoint / --checkpoint-flag
    --policy-port
    --policy-extra-args
  BENCHMARK side:
    --benchmark-repo      : repo path
    --benchmark-entry     : e.g. run_benchmark.py
    --suite / --task      : forwarded as --suite X --task Y (customizable)
    --num-trials
    --benchmark-extra-args

Output (default {policy_repo}/run_evaluation.sh):
  1. boots policy server in background
  2. waits up to POLICY_BOOT_TIMEOUT for a TCP connect to succeed on port
  3. runs the benchmark client, tearing down policy on exit (trap)

This is the artifact a user hands to SLURM / sbatch.
"""
from __future__ import annotations

import argparse
import stat
import sys
from pathlib import Path


TEMPLATE = """#!/usr/bin/env bash
# Auto-generated by generate_run_evaluation.
# End-to-end evaluation: policy server + benchmark client, one script.
set -euo pipefail

# ---- POLICY side ----
POLICY_REPO="{policy_repo}"
POLICY_VENV="$POLICY_REPO/.venv/bin/python3"
POLICY_ENTRY="{policy_entry}"
CHECKPOINT="{checkpoint}"
POLICY_PORT="{policy_port}"

# ---- BENCHMARK side ----
BENCH_REPO="{benchmark_repo}"
BENCH_VENV="$BENCH_REPO/.venv/bin/python3"
BENCH_ENTRY="{benchmark_entry}"
SUITE="{suite}"
TASK="{task}"
NUM_TRIALS="{num_trials}"

BOOT_TIMEOUT={boot_timeout}

# ---- Sanity ----
for f in "$POLICY_VENV" "$BENCH_VENV"; do
  [ -x "$f" ] || {{ echo "ERROR: venv missing at $f"; exit 1; }}
done

# ---- Boot policy server in background ----
echo "=== Booting policy server on port $POLICY_PORT ==="
cd "$POLICY_REPO"
"$POLICY_VENV" -u "$POLICY_ENTRY" \\
  {checkpoint_flag} "$CHECKPOINT" \\
  --port "$POLICY_PORT" {policy_extra_args} \\
  > "/tmp/policy_server_${{POLICY_PORT}}.log" 2>&1 &
POLICY_PID=$!
echo "    PID=$POLICY_PID, log=/tmp/policy_server_${{POLICY_PORT}}.log"

cleanup() {{
  if kill -0 "$POLICY_PID" 2>/dev/null; then
    echo "=== Tearing down policy server (PID=$POLICY_PID) ==="
    kill "$POLICY_PID" 2>/dev/null || true
    wait "$POLICY_PID" 2>/dev/null || true
  fi
}}
trap cleanup EXIT

# ---- Wait for port ----
echo "=== Waiting up to $BOOT_TIMEOUT s for policy TCP port ==="
for i in $(seq 1 "$BOOT_TIMEOUT"); do
  if (echo >/dev/tcp/127.0.0.1/"$POLICY_PORT") 2>/dev/null; then
    echo "    policy reachable after ${{i}}s"
    break
  fi
  sleep 1
  if ! kill -0 "$POLICY_PID" 2>/dev/null; then
    echo "ERROR: policy server died during boot. Tail log:"
    tail -40 "/tmp/policy_server_${{POLICY_PORT}}.log"
    exit 4
  fi
done
if ! (echo >/dev/tcp/127.0.0.1/"$POLICY_PORT") 2>/dev/null; then
  echo "ERROR: policy port never opened within $BOOT_TIMEOUT s"
  tail -40 "/tmp/policy_server_${{POLICY_PORT}}.log"
  exit 5
fi

# ---- Run benchmark ----
echo "=== Running benchmark: $SUITE / $TASK ($NUM_TRIALS trials) ==="
cd "$BENCH_REPO"
exec "$BENCH_VENV" -u "$BENCH_ENTRY" \\
  --policy-port "$POLICY_PORT" \\
  --suite "$SUITE" --task "$TASK" \\
  --num-trials "$NUM_TRIALS" {benchmark_extra_args}
"""


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--policy-repo", required=True, type=Path)
    ap.add_argument("--policy-entry", required=True)
    ap.add_argument("--checkpoint", required=True)
    ap.add_argument("--checkpoint-flag", default="--checkpoint")
    ap.add_argument("--policy-port", type=int, default=18800)
    ap.add_argument("--policy-extra-args", default="")
    ap.add_argument("--benchmark-repo", required=True, type=Path)
    ap.add_argument("--benchmark-entry", required=True)
    ap.add_argument("--suite", default="libero_spatial")
    ap.add_argument("--task", default="0")
    ap.add_argument("--num-trials", type=int, default=10)
    ap.add_argument("--benchmark-extra-args", default="")
    ap.add_argument("--boot-timeout", type=int, default=180)
    ap.add_argument("--out", default="")
    args = ap.parse_args()

    policy_repo = args.policy_repo.resolve()
    bench_repo = args.benchmark_repo.resolve()
    for p in (policy_repo, bench_repo):
        if not p.is_dir():
            print(f"ERROR: dir missing: {p}")
            return 1

    out_path = Path(args.out) if args.out else policy_repo / "run_evaluation.sh"
    content = TEMPLATE.format(
        policy_repo=str(policy_repo),
        policy_entry=args.policy_entry,
        checkpoint=args.checkpoint,
        checkpoint_flag=args.checkpoint_flag,
        policy_port=args.policy_port,
        policy_extra_args=args.policy_extra_args,
        benchmark_repo=str(bench_repo),
        benchmark_entry=args.benchmark_entry,
        suite=args.suite,
        task=args.task,
        num_trials=args.num_trials,
        benchmark_extra_args=args.benchmark_extra_args,
        boot_timeout=args.boot_timeout,
    )
    out_path.write_text(content)
    out_path.chmod(out_path.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
    print(f"[generate_run_evaluation] wrote {out_path}")
    print(f"[generate_run_evaluation] run:  {out_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
